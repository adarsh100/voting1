<div class="panel9">
<p class="exp">The Constitution puts a limit on the size of the Lok Sabha of 550 elected members, apart from two members who can be nominated by the President to represent the Anglo-Indian community. There are also provisions to ensure the representation of scheduled castes and scheduled tribes, with reserved constituencies where only candidates from these communities can stand for election.</p>
</div>
<p class="flip9">5.Reservation of Seats</p>
