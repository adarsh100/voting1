<p class="flip6">2. Indian Elections -Scale of Operation</p>
</div>


<div class="set1">

<div class="panel7">
<p class="exp">The country has been divided into 543 Parliamentary Constituencies, each of which returns one MP to the Lok Sabha, the lower house of the Parliament. The size and shape of the parliamentary constituencies are determined by an independent Delimitation Commission, which aims to create constituencies which have roughly the same population, subject to geographical considerations and the boundaries of the states and administrative areas.</p>
</div>
<p class="flip7">3. Constituencies & Reservation of Seats</p>
</div>
<div class="set1">
<div class="panel8">
<p class="exp">Delimitation is the redrawing of the boundaries of parliamentary or assembly constituencies to make sure that there are, as near as practicable, the same number of people in each constituency. In India boundaries are meant to be examined after the ten-yearly census to reflect changes in population, for which Parliament by law establishes an independent Delimitation Commission, made up of the Chief Election Commissioner and two judges or ex-judges from the Supreme Court or High Court.</p>
</div>
